// Genera 1000 auto aggiungendo varianti e modelli
function generateFullCarDatabase() {
    const fullDatabase = [...CAR_DATABASE];
    
    // Aggiungi varianti per ogni marca
    const brands = ['BMW', 'Mercedes', 'Audi', 'Porsche', 'Ferrari', 'Lamborghini', 'Tesla', 'Maserati', 'Aston Martin', 'McLaren'];
    const types = ['suv', 'sportiva', 'berlina', 'elettrica', 'ibrida', 'cabrio', 'station-wagon'];
    const colors = ['Nero Metallic', 'Bianco Alpine', 'Grigio Storm Bay', 'Blu Tanzanite', 'Rosso Corsa', 'Verde Mantis', 'Giallo Racing'];
    const fuels = ['benzina', 'diesel', 'elettrico', 'ibrido', 'gpl', 'metano'];
    
    let carId = 1000;
    
    for (let i = 0; i < 1000 - CAR_DATABASE.length; i++) {
        const brand = brands[Math.floor(Math.random() * brands.length)];
        const type = types[Math.floor(Math.random() * types.length)];
        const color = colors[Math.floor(Math.random() * colors.length)];
        const fuel = fuels[Math.floor(Math.random() * fuels.length)];
        const year = 2020 + Math.floor(Math.random() * 5);
        const price = 50000 + Math.floor(Math.random() * 500000);
        const power = 200 + Math.floor(Math.random() * 800);
        const engine = fuel === 'elettrico' ? 0 : 1000 + Math.floor(Math.random() * 5000);
        
        const car = {
            id: `${brand.toLowerCase()}-${carId++}`,
            brand: brand,
            model: `${brand} Model ${String.fromCharCode(65 + (i % 26))}${Math.floor(i / 26) + 1}`,
            year: year,
            price: price,
            type: type,
            color: color,
            km: Math.floor(Math.random() * 50000),
            fuel: fuel,
            engine: engine,
            power: power,
            transmission: Math.random() > 0.5 ? 'automatico' : 'manuale',
            doors: type === 'sportiva' ? 2 : 5,
            seats: type === 'sportiva' ? 2 : 5,
            description: `${brand} ${type} con motore ${fuel === 'elettrico' ? 'elettrico' : fuel} da ${power} CV`,
            image: 'https://images.unsplash.com/photo-1555215695-3004980ad54e?w=800',
            status: 'active',
            featured: Math.random() > 0.8,
            externalLink: `https://www.${brand.toLowerCase()}.com/it/models/${type}`
        };
        
        fullDatabase.push(car);
    }
    
    return fullDatabase;
}

// Esporta il database completo
window.CAR_DATABASE_FULL = generateFullCarDatabase();
